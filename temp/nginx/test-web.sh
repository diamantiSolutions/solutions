#!/bin/sh  

replicas=$1;


#test is assuming that you might have made change in config map of lb and webserver, so reloadign them before satrtign traffic


kubectl delete all -l app=wrk -n nginx-lb



echo "Waiting for wrk pods to be deleted!"
while true ; do
    printf "." 
    result=$(kubectl get  pods -o wide -n nginx-lb | grep -c 'wrk-rc.*') 
    if [ $result  -eq 0 ] ; then
	echo " all wrk pods are deleted!"
	break
    fi
    sleep 2
done



kubectl replace -f nginx-configmap.yaml


itration=1

for mask in 0000000000000000000000000000000000000001 0000000000000000000000000000000000000010 0000000000000000000000000000000000000100 0000000000000000000000000000000000001000 0000000000000000000000000000000000010000 0000000000000000000000000000000000100000 0000000000000000000000000000000001000000 0000000000000000000000000000000010000000 0000000000000000000000000000000100000000 0000000000000000000000000000001000000000 0000000000000000000000000000010000000000 0000000000000000000000000000100000000000 0000000000000000000000000001000000000000 0000000000000000000000000010000000000000 0000000000000000000000000100000000000000 0000000000000000000000001000000000000000 0000000000000000000000010000000000000000 0000000000000000000000100000000000000000 0000000000000000000001000000000000000000 0000000000000000000010000000000000000000 0000000000000000000100000000000000000000 0000000000000000001000000000000000000000 0000000000000000010000000000000000000000 0000000000000000100000000000000000000000 0000000000000001000000000000000000000000 0000000000000010000000000000000000000000 0000000000000100000000000000000000000000 0000000000001000000000000000000000000000 0000000000010000000000000000000000000000 0000000000100000000000000000000000000000 0000000001000000000000000000000000000000 0000000010000000000000000000000000000000 0000000100000000000000000000000000000000 0000001000000000000000000000000000000000 0000010000000000000000000000000000000000 0000100000000000000000000000000000000000 0001000000000000000000000000000000000000 0010000000000000000000000000000000000000 0100000000000000000000000000000000000000 1000000000000000000000000000000000000000
do
  sed "s/<CPU_AFFINITY>/$mask/g" nginx-lb-configmap.yaml | sed "s/<lb_num>/$itration/g" | kubectl replace -f -
  ((itration++))

    
done




for (( number=1; number <= $replicas; ++number ))
do
     sed -e "s~<numcore>~1~g" wrk-rc-web.yaml | sed "s/<lb_num>/$number/g" |kubectl create -f - 
done



echo "Waiting for wrk pods to be created!"
while true ; do
    printf "." 
    result=$(kubectl get  pods -o wide -n nginx-lb | grep -c 'wrk-rc.*Running') 
    if [ $result  -eq $replicas ] ; then
	echo " all wrk pods are up and running!"
	break
    fi
    sleep 5
done


#sleep 700s 

echo "waiting for results from all wrk pods!"
while true ; do 
    printf "."
    result=$(kubectl logs  --tail=0  -l app=wrk -n nginx-lb | grep -c "Requests/sec") # -n shows line number
    if [ $result  -eq $replicas ] ; then
	echo " "
	echo "==> test with $replicas instances produced following results:"
	rps=$(kubectl logs  --tail=0  -l app=wrk -n nginx-lb | grep -e "Requests/sec")
	perl calculate.pl -s="$rps"
	break
    fi
    sleep 10
done





exit 0
